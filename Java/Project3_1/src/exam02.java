
//exam02 class 정의 (프로젝트 파일 이름과 동일해야함)
public class exam02 {

	public static void main(String[] args) {
		int var1 = 10;             // 정수형 변수를 선언하고 값을 대입
		float var2 = 10.1f;        // 실수형 변수를 선언, float 타입에 값을 대입할때는 값뒤에 f를 붙여줘야 함 
		double var3 = 10.2;        // 실수형 변수를 선언 
		char var4 = '안';           // char 타입은 문자 1개를 ''로 묶어 대입해야 함 
		String var5 = "안드로이드";    // 문자열은 String으로 선언하고, ""로 묶어야 함 

		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		System.out.println(var5);

	}

}